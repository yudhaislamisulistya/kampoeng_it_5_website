<?php

namespace App\Http\Controllers;

use App\Post;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Auth;

class PostController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */

    public function __construct()
    {
        $this->middleware('RoleAdmin');
    }

    public function index()
    {
        $dataauth = \App\User::find(Auth::id());
        $datapost = \App\Post::all();
        return view('admin.post', compact('dataauth', 'datapost'));
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $dataauth = \App\User::find(Auth::id());
        return view('admin.buatpost', compact('dataauth'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $post = new Post;
        $post->title = $request->title;
        $post->content = $request->content;
        $post->file = $request->file->getClientOriginalName();
        if ($post->save()) {
            $request->file->move('file_post', $request->file->getClientOriginalName());
            return redirect()->route('admin_post_utama');
        } else {
            return redirect()->route('admin_post_buat');
        }
    }

